<h3>Subspecies</h3>

<ul>
    <li>The nominate subspecies Ailuropoda m. melanoleuca consists of most extant populations of panda.</li>
	<li>The <a href="https://en.wikipedia.org/wiki/Qinling_panda">Qinling panda</a>, A. m. qinlingensis is restricted to the Qinling Mountains in Shaanxi at elevations of 1,300–3,000 m. </li>

</ul>

