<h3>Description</h3>

<ol>
<li>Adults measure around <strong>1.2</strong> to <strong>1.9 m</strong> <strong>(4 to 6 ft)</strong> long.</li>
<li>Males can weigh up to <strong>160 kg (350 lb)</strong>.</li>
<li>The giant panda has a body shape typical of bears.</li>
<li>It has <strong>black fur</strong> on its ears, eye patches, muzzle, legs, arms and shoulders. </li>
<li>The rest of the animal's coat is <strong>white</strong>.</li>
</ol>

